public class Apartamento {
    String endereco;
    int numQuartos;
    double area;
    double aluguel;

    public Apartamento(String endereco, int numQuartos, double area, double aluguel) {
        this.endereco = endereco;
        this.numQuartos = numQuartos;
        this.area = area;
        this.aluguel = aluguel;
    }
}
